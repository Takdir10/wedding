        const rootElement = document.querySelector(":root");
        const audioIcon = document.querySelector('.audio-icon');
        const song = document.querySelector('#bg-song');
        const icon = document.querySelector('.audio-icon i');
        const nav = document.querySelector('.mynavbar');
        const contain = document.querySelector('.container')
        let isPlaying = false;

      function disableScroll() {
        const scrollTop = window.pageYOffset || document.documentElement.scrollTop;
        const scrollLeft = window.pageXOffset || document.documentElement.scrollLeft;

        window.onscroll = function() {
          window.scrollTo(scrollTop, scrollLeft);
        }

        rootElement.style.scrollBehavior = 'auto';
      }

      function enableScroll() {
        window.onscroll = function() { }
        rootElement.style.scrollBehavior = 'smooth';
        // localStorage.setItem('opened', 'true');
        playAudio();
        nav.classList.add('fixed-bottom');
        contain.style.display = 'flex';
      }

      function playAudio() {
        song.volume = 0.5;
        audioIcon.style.display = 'flex'
        song.play();
        isPlaying = true;
      }

      audioIcon.onclick = function() {
        if (isPlaying) {
          song.pause();
          icon.classList.remove('bi-disc');
          icon.classList.add('bi-pause-circle');
        } else {
          song.play();
          icon.classList.add('bi-disc');
          icon.classList.remove('bi-pause-circle');
        }

      isPlaying = !isPlaying;
      }

      // if(!localStorage.getItem('opened')) {
      //   disableScroll();
      // }
      disableScroll();
    </script>

